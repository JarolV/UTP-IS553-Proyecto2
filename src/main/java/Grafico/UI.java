    public boolean ObtenerCuenta() throws campoInvalido{
        if(jTextNombre.getText().trim().isBlank()){
            throw new campoInvalido("El nombre no puede ir en blanco");
        }
        if(jTextCedula.getText().trim().isBlank()){
            throw new campoInvalido("El nombre no puede ir en blanco");
        }
        if(!jTextCedula.getText().matches("[0-9]+")){
            throw new campoInvalido("La cedula debe de ser numerica");
        }
        if(jTextCuenta.getText().trim().isBlank()){
            throw new campoInvalido("Debe ingresar un numero de cuenta");
        }
        if(!jTextClave.getText().matches("[0-9]+")){
            throw new campoInvalido("La clave debe de ser numerica");
        }
        if(!(jTextClave.getText().trim().length()==4)){
            throw new campoInvalido("La clave debe de ser de 4 digitos");
        }
        return true;
    }
    public boolean ObtenerConsignacion() throws campoInvalido{
        if(jTextCuentaConsignacion.getText().trim().isBlank()){
            throw new campoInvalido("Debe ingresar un numero de cuenta");
        }
        if(!jTextMontoConsignacion.getText().matches("[0-9]+")){
            throw new campoInvalido("La consignacion debe de ser numerica");
        }
        if(!Integer.valueOf(jTextConfirmarMontoConsignacion.getText().trim()).equals(Integer.valueOf(jTextMontoConsignacion.getText().trim()))){
            throw new campoInvalido("Los montos NO coinciden");
        }
        return true;
    }
